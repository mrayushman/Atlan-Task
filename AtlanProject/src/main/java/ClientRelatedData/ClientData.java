package ClientRelatedData;

public class ClientData {

    private String clientEmail;
    private String clientName;
    private double incomePerAnnum;
    private double savingsPerAnnum;
    private String mobileNumber;

    // Getters and setters

    public String getClientEmail() {
        return clientEmail;
    }

    public void setClientEmail(String clientEmail) {
        this.clientEmail = clientEmail;
    }

    public String getClientName() {
        return clientName;
    }

    public void setClientName(String clientName) {
        this.clientName = clientName;
    }

    public double getIncomePerAnnum() {
        return incomePerAnnum;
    }

    public void setIncomePerAnnum(double incomePerAnnum) {
        this.incomePerAnnum = incomePerAnnum;
    }

    public double getSavingsPerAnnum() {
        return savingsPerAnnum;
    }

    public void setSavingsPerAnnum(double savingsPerAnnum) {
        this.savingsPerAnnum = savingsPerAnnum;
    }

    public String getMobileNumber() {
        return mobileNumber;
    }

    public void setMobileNumber(String mobileNumber) {
        this.mobileNumber = mobileNumber;
    }
}
